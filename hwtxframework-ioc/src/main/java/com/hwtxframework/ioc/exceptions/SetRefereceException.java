package com.hwtxframework.ioc.exceptions;

public class SetRefereceException extends RuntimeException {

	private static final long serialVersionUID = 2126102133505244665L;

	public SetRefereceException() {
		super();
	}

	public SetRefereceException(String message, Throwable cause) {
		super(message, cause);
	}

	public SetRefereceException(String message) {
		super(message);
	}

	public SetRefereceException(Throwable cause) {
		super(cause);
	}

}
